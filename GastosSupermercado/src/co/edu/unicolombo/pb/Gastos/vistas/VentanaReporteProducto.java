    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
      Collection<Producto> datos = Producto.ProductosBD.values();
ArrayList<Producto> listaProductos = new ArrayList<>(datos);

String columnas[] = {"CÃ“DIGO", "NOMBRE", "PRECIO"};
int totalProductos = listaProductos.size();
String filas[][] = new String[totalProductos][columnas.length];

for (int i = 0; i < listaProductos.size(); i++) {
    Producto p = listaProductos.get(i);
    filas[i][0] = p.id;
    filas[i][1] = p.nombre;
    filas[i][2] = String.valueOf(p.valor);
}

DefaultTableModel datosTablon = new DefaultTableModel();
datosTablon.setDataVector(filas, columnas);
TablonProducto.setModel(datosTablon);
txtTotalProducto.setText(String.valueOf(totalProductos));
   
    }//GEN-LAST:event_formWindowOpened

